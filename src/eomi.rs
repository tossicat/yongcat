//! ## 어미 모듈
//! 이 모듈에는 `yongcat`이 처리할 수 있는 어미 목록들을 가지고 있습니다.
//!
//! 각 어미는 붙일 단어에 따라 변환할 때, 변환하기 쉽게 하기 위하여
//! 양성모음, 음성모음, "하다"용 세 가지 형태를 갖습니다.
//! 이는 이전 `tossicat`에서 조사의 (&str, &str, &str) 튜플 컨벤션과 동일한 아이디어입니다.

/// (양성모음 형태, 음성모음 형태, "하다"용 형태)
pub type AhEoForm = (&'static str, &'static str, &'static str);

/// 어미 그룹을 나타내는 열거형입니다.
///
/// 어미의 종류에 따라 어간과 결합하는 규칙이 다르므로,
/// 그룹별로 구분하여 `join`과 `merge` 모듈에서 분기 처리합니다.
pub enum EomiGroup {
    /// 아/어 계열: 모음조화에 따라 형태가 달라집니다.
    AhEo(AhEoForm),
    /// 받침 유무에 따라 형태가 달라집니다. (받침 있을 때, 없을 때)
    Plain(&'static str, &'static str),
    /// 형태가 고정된 어미입니다.
    Fixed(&'static str),
}

/// ### 아/어 계열 어미
/// 모음조화에 따라 형태가 달라지는 아/어 계열 어미를 정의하고 있습니다.
/// "아/어" 계열 어미: 모두 동일한 모음조화 + 축약 규칙을 따른다.
pub const AH_EO_GROUP: &[AhEoForm] = &[
    ("아", "어", "여"),       // 종결 (해라체): 가 + 아 → 가, 먹 + 어 → 먹어
    ("아요", "어요", "여요"), // 종결 (해요체): 가 + 아요 → 가요, 먹 + 어요 → 먹어요
    ("아서", "어서", "여서"), // 연결 (이유/순서): 가 + 아서 → 가서, 먹 + 어서 → 먹어서
    ("아도", "어도", "여도"), // 연결 (양보): 가 + 아도 → 가도, 먹 + 어도 → 먹어도
    ("아야", "어야", "여야"), // 연결 (조건): 가 + 아야 → 가야, 먹 + 어야 → 먹어야
    ("아라", "어라", "여라"), // 명령: 가 + 아라 → 가라, 먹 + 어라 → 먹어라
    ("았", "었", "였"),       // 과거 시제: 가 + 았 → 갔, 먹 + 었 → 먹었
];
