//! ## 아/어 계열 어미
//! 모음조화에 따라 형태가 달라지는 아/어 계열 어미를 정의하고 있습니다.
//!
//! 각 상수는 양성모음 형태의 로마자 음차로 명명합니다.

use super::Eomi;

/// 아/어 — 종결 (해체, 반말): 가 + 아 → 가, 먹 + 어 → 먹어
pub const A: Eomi = Eomi::AhEo(("아", "어"));

/// 아요/어요 — 종결 (해요체): 가 + 아요 → 가요, 먹 + 어요 → 먹어요
pub const AYO: Eomi = Eomi::AhEo(("아요", "어요"));

/// 아서/어서 — 연결 (이유/순서): 가 + 아서 → 가서, 먹 + 어서 → 먹어서
pub const ASEO: Eomi = Eomi::AhEo(("아서", "어서"));

/// 아도/어도 — 연결 (양보): 가 + 아도 → 가도, 먹 + 어도 → 먹어도
pub const ADO: Eomi = Eomi::AhEo(("아도", "어도"));

/// 아야/어야 — 연결 (조건): 가 + 아야 → 가야, 먹 + 어야 → 먹어야
pub const AYA: Eomi = Eomi::AhEo(("아야", "어야"));

/// 아라/어라 — 명령: 가 + 아라 → 가라, 먹 + 어라 → 먹어라
pub const ARA: Eomi = Eomi::AhEo(("아라", "어라"));

/// 았/었 — 과거 시제: 가 + 았 → 갔, 먹 + 었 → 먹었
///
/// "았"의 로마자 음차(ASS)가 영어 비속어와 동일하지만,
/// 한국어 음차 규칙을 일관되게 적용한 것입니다.
pub const ASS: Eomi = Eomi::AhEo(("았", "었"));

/// 았어요/었어요 — 과거 해요체: 가 + 았어요 → 갔어요, 먹 + 었어요 → 먹었어요
pub const ASS_EOYO: Eomi = Eomi::AhEo(("았어요", "었어요"));

/// 았습니다/었습니다 — 과거 합쇼체: 가 + 았습니다 → 갔습니다, 먹 + 었습니다 → 먹었습니다
pub const ASS_SEUMNIDA: Eomi = Eomi::AhEo(("았습니다", "었습니다"));

/// 아지다/어지다 — 피동·상태변화: 좋 + 아지다 → 좋아지다, 커 + 어지다 → 커지다
pub const AJIDA: Eomi = Eomi::AhEo(("아지다", "어지다"));

/// 아보다/어보다 — 시행: 가 + 아보다 → 가보다, 먹 + 어보다 → 먹어보다
pub const ABODA: Eomi = Eomi::AhEo(("아보다", "어보다"));

/// 아달라/어달라 — 요청: 읽 + 어달라 → 읽어달라, 가 + 아달라 → 가달라
pub const ADALLA: Eomi = Eomi::AhEo(("아달라", "어달라"));

/// 아주다/어주다 — 수혜: 가 + 아주다 → 가주다, 먹 + 어주다 → 먹어주다
pub const AJUDA: Eomi = Eomi::AhEo(("아주다", "어주다"));